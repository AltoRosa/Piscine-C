#include <unistd.h>

void	ft_putchar(char c);

void	ft_putstr(char *str)
{
	int i;

	i = 0;
	while (str[i])
	{
		ft_putchar(str[i]);
		i++;
	}
}

void	ft_print_reverse_alphabet(void)
{
	ft_putstr("zyxwvutsrqponmlkjihgfedcba");
}
